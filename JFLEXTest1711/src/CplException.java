
public class CplException extends Exception {
	private static final long serialVersionUID = -3172129566171423617L;

	public CplException() {
		super();
	}

	public CplException(String message) {
		super(message);
	}

	public CplException(String message, Throwable cause) {
		super(message, cause);
	}

	public CplException(Throwable cause) {
		super(cause);
	}
}
